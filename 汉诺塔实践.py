steps = 0
def hanoi(n, src, des, mid):
    global steps
    if n == 1:
        steps += 1
        print("[STEP{:>4}] {}->{}".format(steps, src, des))
    else:
        hanoi(n - 1, src, mid, des)
        steps += 1
        print("[STEP{:>4}] {}->{}".format(steps, src, des))
        hanoi(n - 1, mid, des, src)

