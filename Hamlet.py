#请在...处补充代码

def getText():
    txt = open("hamlet.txt", "r").read()
    txt = txt.lower()
    for ch in '!"#$%&()*+,-./:;<=>?@[\\]^_‘{|}~':
        txt = txt.replace(ch, " ")
    return txt

hamletTxt = getText()
words = hamletTxt.split()
counts = {}
for word in words:
    counts[word] = counts.get(word, 0) + 1
items = list(counts.items())
#自定义排序，lambda。根据每个数组的第二个数字进行从大到小排列
items.sort(key=lambda x:x[1], reverse = True)
for i in range(10):
    word,count = items[i]
    print(word)